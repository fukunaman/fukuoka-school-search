export function getTownList(ward: string): string[] {
  if (!ward || !schoolData[ward]) {
    return [];
  }
  return sortByReading(Object.keys(schoolData[ward]), townReadings);
}
export function getChomeList(ward: string, town: string): string[] {
  if (!ward || !town || !schoolData[ward] || !schoolData[ward][town]) {
    return [];
  }
  return Object.keys(schoolData[ward][town]).sort();
}
export function getAllTowns(): Array<{ ward: string; town: string }> {
  const allTowns: Array<{ ward: string; town: string }> = [];
  for (const ward in schoolData) {
    for (const town in schoolData[ward]) {
      allTowns.push({ ward, town });
    }
  }
  return allTowns.sort((a, b) => a.town.localeCompare(b.town, 'ja'));
}
export function getAllSchools(): Array<{ name: string; type: 'elementary' | 'middle' }> {
  const schools: Set<string> = new Set();
  const schoolList: Array<{ name: string; type: 'elementary' | 'middle' }> = [];
  Object.keys(schoolData).forEach((ward) => {
    Object.keys(schoolData[ward]).forEach((town) => {
      Object.keys(schoolData[ward][town]).forEach((chome) => {
        const schoolInfo = schoolData[ward][town][chome];
        if (!schools.has(schoolInfo.elementary)) {
          schools.add(schoolInfo.elementary);
          schoolList.push({
            name: schoolInfo.elementary,
            type: 'elementary',
          });
        }
        if (!schools.has(schoolInfo.middle)) {
          schools.add(schoolInfo.middle);
          schoolList.push({
            name: schoolInfo.middle,
            type: 'middle',
          });
        }
      });
    });
  });
  return schoolList.sort((a, b) => a.name.localeCompare(b.name, 'ja'));
}
export function searchSchoolAreas(
  schoolName: string
): Array<{
  ward: string;
  town: string;
  chome: string;
  area: string;
  elementary: string;
  middle: string;
  highSchoolDistrict?: string;
}> {
  const results: Array<{
    ward: string;
    town: string;
    chome: string;
    area: string;
    elementary: string;
    middle: string;
    highSchoolDistrict?: string;
  }> = [];
  Object.keys(schoolData).forEach((ward) => {
    Object.keys(schoolData[ward]).forEach((town) => {
      Object.keys(schoolData[ward][town]).forEach((chome) => {
        const schoolInfo = schoolData[ward][town][chome];
        if (schoolInfo.elementary === schoolName || schoolInfo.middle === schoolName) {
          results.push({
            ward,
            town,
            chome: chome === '' ? '(町名のみ)' : chome,
            area: chome,
            elementary: schoolInfo.elementary,
            middle: schoolInfo.middle,
            highSchoolDistrict: getHighSchoolDistrict(ward, schoolInfo.middle),
          });
        }
      });
    });
  });
  return results.sort((a, b) => {
    if (a.ward !== b.ward) return a.ward.localeCompare(b.ward, 'ja');
    if (a.town !== b.town) return a.town.localeCompare(b.town, 'ja');
    if (a.chome !== b.chome) return a.chome.localeCompare(b.chome, 'ja');
    return a.area.localeCompare(b.area, 'ja');
  });
}
function getHighSchoolDistrict(ward: string, middleSchool: string): string | undefined {
  if (ward === '南区' && middleSchool === '長丘中') {
    return '第５学区または第６学区';
  }
  const key = `${ward}:${middleSchool}`;
  return highSchoolData[key];
}
export function searchSchool(ward: string, town: string, chome: string): SearchResult | null {
  if (!ward || !town || !schoolData[ward] || !schoolData[ward][town]) {
    return null;
  }
  let result = null;
  if (schoolData[ward][town][chome]) {
    result = schoolData[ward][town][chome];
  } else if (schoolData[ward][town]['']) {
    result = schoolData[ward][town][''];
  }
  if (result) {
    const searchResult: SearchResult = {
      elementary: result.elementary,
      middle: result.middle,
      note: result.note,
    };
    const highSchoolDistrict = getHighSchoolDistrict(ward, result.middle);
    if (highSchoolDistrict) {
      searchResult.highSchoolDistrict = highSchoolDistrict;
    }
    return searchResult;
  }
  return null;
}
export function searchTownAreas(
  ward: string,
  town: string
): Array<{
  ward: string;
  town: string;
  chome: string;
  elementary: string;
  middle: string;
  highSchoolDistrict?: string;
}> {
  const areas: Array<{
    ward: string;
    town: string;
    chome: string;
    elementary: string;
    middle: string;
    highSchoolDistrict?: string;
  }> = [];
  if (!ward || !town || !schoolData[ward] || !schoolData[ward][town]) {
    return areas;
  }
  Object.keys(schoolData[ward][town]).forEach((chomeKey) => {
    const schoolInfo = schoolData[ward][town][chomeKey];
    const highSchoolDistrict = getHighSchoolDistrict(ward, schoolInfo.middle);
    areas.push({
      ward,
      town,
      chome: chomeKey === '' ? '全域' : chomeKey,
      elementary: schoolInfo.elementary,
      middle: schoolInfo.middle,
      highSchoolDistrict,
    });
  });
  return areas;
}
