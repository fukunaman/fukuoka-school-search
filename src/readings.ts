// 読み仮名でソートするヘルパー関数
export function sortByReading<T extends string>(
  items: T[],
  readingsMap: Record<string, string>
): T[] {
  return items.sort((a, b) => {
    const readingA = readingsMap[a] || a;
    const readingB = readingsMap[b] || b;
    return readingA.localeCompare(readingB, 'ja', { sensitivity: 'base' });
  });
}
